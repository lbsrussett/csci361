	.globl binary_search
binary_search:
	// LOAD THE CONSTANT VALUE 2 INTO A TEMPORARY REGISTER
	LDUR X9, [X9, #2]
	// DIVIDE THE LENGTH OF THE ARRAY BY 2 AND STORE IN X15
	UDIV X10, X1, X9
	// USE LSL TO MULTIPLY 1/2 LENGTH BY 8 TO FIND OFFSET OF MIDDLE ELEMENT
	LSL X10, X10, #3
	// ADD THE OFFSET VALUE TO THE BASE ADDRESS OF THE ARRAY
	ADD X10, X10, X0
	// LOAD THE ELEMENT AT THE ADDRESS
	LDUR X11, [X10, #8]






	br	X30
